'''
Q2.Write a program, in which create variables
a = 367
b = 255
and perform all the arithmetic functions on them.
'''
a, b = 367, 255

sum = a + b
print('sum: ',sum)

sub = a - b
print('\n \nsubtraction: ',sub)

product = a * b
print('\n \nProduct: ',product)

div = a/b
print('\n \nDivision: ',div)

mod = a%b
print('\n \nmod: ',mod)

expo = a**b
print('\n \nExponentiation: ',expo)

floordiv = a//b
print('\n \nFloordivison: ',floordiv)